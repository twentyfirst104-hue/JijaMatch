"""
ai/tags.py — фиксированный справочник тегов.

ВАЖНО: ИИ обязан выбирать теги ТОЛЬКО из этих списков. Любой тег вне списка
игнорируется при сохранении (см. ai/tagger.py). Это защищает алгоритм подбора
от «фантазий» модели и держит набор измерений профиля стабильным.

Три группы тегов:
1) PROFILE_TAGS  — вкусовой профиль, бинарный признак (есть/нет -> value 0 или 1).
2) PROPERTY_TAGS — свойства, числовая шкала 0..10.
3) MOOD_TAGS     — настроение/повод, бинарный признак (есть/нет -> 0 или 1).
"""

# Вкусовой профиль (наличие: есть/нет)
PROFILE_TAGS: list[str] = [
    "фруктовый",
    "ягодный",
    "цитрусовый",
    "тропический",
    "десертный",
    "кондитерский",
    "табачный",
    "ментоловый",
    "кофейный",
    "ореховый",
    "цветочный",
    "напиточный",
    "пряный",
    "молочный",
    "карамельный",
    "шоколадный",
    "мятный",
    "экзотика",
]

# Свойства (числовая шкала 0..10)
PROPERTY_TAGS: list[str] = [
    "сладость",
    "кислотность",
    "свежесть",
    "холод",
    "насыщенность",
    "мягкость",
]

# Настроение / повод (наличие: есть/нет)
MOOD_TAGS: list[str] = [
    "бодрящий",
    "расслабляющий",
    "согревающий",
    "освежающий",
    "тонизирующий",
    "уютный",
]

# Полный набор имён тегов (для валидации и кеша)
ALL_TAGS: list[str] = PROFILE_TAGS + PROPERTY_TAGS + MOOD_TAGS

# Множества для быстрых проверок «принадлежит ли тег группе»
PROFILE_TAGS_SET = set(PROFILE_TAGS)
PROPERTY_TAGS_SET = set(PROPERTY_TAGS)
MOOD_TAGS_SET = set(MOOD_TAGS)
ALL_TAGS_SET = set(ALL_TAGS)


def is_property_tag(tag_name: str) -> bool:
    """True, если тег — свойство (числовая шкала 0..10)."""
    return tag_name in PROPERTY_TAGS_SET


def is_known_tag(tag_name: str) -> bool:
    """True, если тег есть в любом из справочников."""
    return tag_name in ALL_TAGS_SET
